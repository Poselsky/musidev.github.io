class MessageBuilder {
    constructor(friendName, imageSource) {
        this.friendName = friendName;
        this.imageSource = imageSource;
        this.htmlString = "";
    }

    addFriendMessage(message) {
        this.htmlString += `
        <div class="flex_direction_row flex_align_items_start">
            <img class="img_preset_medium img_preset_circle"
            src="${this.imageSource}" style="margin:10px 0px 10px 20px"/>
            <div style="margin-right:40px">
                <p class="main_name_middle_row custom_font custom_font_large color_font_dark">${this.friendName}</p>
                <p class="main_text_middle_row custom_font custom_font_large color_font_light">${message}</p>
            </div>
        </div>
        `;

        return this;
    }

    addResponseBar() {
        this.htmlString += `
            <div id="message_a_friend" class="flex flex_align_items_center custom_font custom_font_large" style="margin-bottom:5px">
                <input id="searchbar" class="color_background_dark color_font_medium" type="search"
                placeholder="Message @${this.friendName}">
            </div> 
        `;
    }

    buildMessages() {
        this.addResponseBar();
        return `
            <div class="flex flex_direction_column flex_justify_content_end h-100p">
                ${this.htmlString}
            </div>
        `;
    }
}
